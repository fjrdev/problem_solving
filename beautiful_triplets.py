def beautifulTriplets(d, arr):
    # Write your code here
    ctr = 0
    dict = {}
    for i in arr:
        dict[i] = i
    for i in arr:
        if dict.get((i+d), 0) != 0 and dict.get((i+2*d), 0) != 0:
            ctr += 1
    return ctr

